/**
 * GET /api/orders
 * Return order history (orders and their items) from the Order Service database.
 * The SQL produces a flat join result which we reshape into a nested JSON structure.
 */
app.get('/api/orders', async (req, res) => {
    try {
        const [rows] = await pool.query(
            `SELECT
                o.id AS order_id,
                o.customer_name,
                o.total_amount,
                o.created_at,
                oi.id AS order_item_id,
                oi.product_id,
                oi.quantity,
                oi.unit_price
            FROM orders o
            LEFT JOIN order_items oi ON oi.order_id = o.id
            ORDER BY o.created_at DESC, oi.id ASC`
        );

        // Convert the flat JOIN result into a nested JSON structure:
        // [ { id, customer_name, total_amount, created_at, items: [...] }, ... ]
        const ordersById = new Map();

        for (const row of rows) {
            if (!ordersById.has(row.order_id)) {
                ordersById.set(row.order_id, {
                    id: row.order_id,
                    customer_name: row.customer_name,
                    total_amount: Number(row.total_amount),
                    created_at: row.created_at,
                    items: []
                });
            }

            // If there are no items for an order ,
            // LEFT JOIN will return nulls
            if (row.order_item_id) {
                ordersById.get(row.order_id).items.push({
                    id: row.order_item_id,
                    product_id: row.product_id,
                    quantity: row.quantity,
                    unit_price: Number(row.unit_price)
                });
            }
        }

        return res.status(200).json({
            success: true,
            orders: Array.from(ordersById.values())
        });
    } catch (err) {
        return res.status(500).json({
            success: false,
            message: 'Failed to fetch order history.',
            error: err.message
        });
    }
});


/**
 * POST /api/orders
 * Create a new order.
 */
app.post('/api/orders', async (req, res) => {
    // STEP 1: INITIAL REQUEST VALIDATION
    const validation = validateCreateOrderPayload(req.body);
    if (!validation.ok) {
        return res.status(400).json({ success: false, message: validation.message });
    }

    if (!INVENTORY_SERVICE_URL) {
        return res.status(500).json({
            success: false,
            message: 'Order Service misconfiguration: INVENTORY_SERVICE_URL is missing.'
        });
    }

    // STEP 2: CONSOLIDATE CLIENT QUANTITIES
    // Merge duplicate product entries so we operate on aggregated quantities
    const requestedQtyById = consolidateItems(req.body.items);

    try {
        // STEP 3: FETCH CATALOG DATA & BUILD ORDER
        // Fetch product prices from Inventory (source of truth) and prepare order payloads
        const products = await fetchInventoryProducts();
        const productById = new Map(products.map((p) => [Number(p.id), p]));
        const orderData = buildOrderData(requestedQtyById, productById);

        if (!orderData.ok) {
            return res.status(400).json({ success: false, message: orderData.message });
        }

        // STEP 4: REMOTE INVENTORY VALIDATION (and deduction)
        // Ask Inventory Service to validate stock (it runs its own transactional deduction)
        const stockResponse = await axios.post(
            `${INVENTORY_SERVICE_URL}/api/products/validate-stock`,
            { items: orderData.inventoryItems },
            { timeout: 5000 }
        );

        // STEP 5: PERSIST ORDER LOCALLY (single DB transaction)
        // Use a bulk insert for order items to reduce network roundtrips (O(1) per order)
        let connection;
        try {
            connection = await pool.getConnection();
            await connection.beginTransaction();

            const customerName = req.body.customer_name || null;

            // Insert order header
            const [orderResult] = await connection.query(
                'INSERT INTO orders (customer_name, total_amount) VALUES (?, ?)',
                [customerName, orderData.totalAmount]
            );

            const orderId = orderResult.insertId;

            // Prepare bulk rows: [order_id, product_id, quantity, unit_price]
            const orderItemRows = orderData.orderItems.map((it) => [orderId, it.product_id, it.quantity, it.unit_price]);

            if (orderItemRows.length > 0) {
                // Single bulk INSERT using parameter placeholder for multiple rows
                await connection.query(
                    'INSERT INTO order_items (order_id, product_id, quantity, unit_price) VALUES ?',
                    [orderItemRows]
                );
            }

            await connection.commit();

            // STEP 6: FINAL RESPONSE
            return res.status(201).json({
                success: true,
                message: 'Order created successfully.',
                order: {
                    id: orderId,
                    customer_name: customerName,
                    total_amount: orderData.totalAmount,
                    items: orderData.orderItems
                },
                inventory: stockResponse.data
            });
        } catch (dbErr) {
            // Attempt to rollback the DB transaction locally
            if (connection) {
                try {
                    await connection.rollback();
                } catch (_) {}
            }

            // COMPENSATING ACTION: Inventory was already deducted by the Inventory Service
            // before we attempted to persist the local order. If the local DB failed,
            // we must attempt to restore stock by calling the Inventory Service's
            // `/api/products/release-stock` endpoint with the same items.
            try {
                await axios.post(
                    `${INVENTORY_SERVICE_URL}/api/products/release-stock`,
                    { items: orderData.inventoryItems },
                    { timeout: 5000 }
                );
            } catch (releaseErr) {
                // Log the compensating rollback failure - this is important for
                // operators to detect and reconcile inventory/order mismatch.
                try {
                    console.error('Compensating rollback failed: unable to release inventory after DB error.',
                        releaseErr.response ? releaseErr.response.data : releaseErr.message || releaseErr);
                } catch (logErr) {
                    // Ensure logging itself never throws
                    console.error('Error while logging compensating rollback failure:', logErr);
                }
            }

            return res.status(500).json({ success: false, message: 'Failed to create order due to a database error.', error: dbErr.message });
        } finally {
            if (connection) connection.release();
        }
    } catch (err) {
        // If Inventory Service returned a structured error, forward that response
        if (err.response) {
            return res.status(err.response.status).json({ success: false, message: 'Inventory check failed.', inventory: err.response.data });
        }

        // Generic fallback when Inventory Service is unreachable
        return res.status(503).json({ success: false, message: 'Inventory Service is unavailable. Please try again later.' });
    }
});

// Return JSON 404 for unknown routes
app.use((req, res) => {
    return res.status(404).json({
        success: false,
        message: 'Route not found.'
    });
});

// Graceful shutdown handler
async function shutdown(signal) {
    try {
        console.log(`\n${signal} received: closing DB pool...`);
        await pool.end();
        console.log('DB pool closed. Exiting.');
        process.exit(0);
    } catch (err) {
        console.error('Error during shutdown:', err);
        process.exit(1);
    }
}

process.on('SIGINT', () => shutdown('SIGINT'));
process.on('SIGTERM', () => shutdown('SIGTERM'));

app.listen(PORT, () => {
    console.log(`✅ Order Service is running on http://localhost:${PORT}`);
});
