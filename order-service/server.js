const express = require('express');
const cors = require('cors');
const mysql = require('mysql2/promise');
const axios = require('axios');

require('dotenv').config();

const app = express();
app.use(express.json());
app.use(cors());

// Service port
const PORT = Number(process.env.PORT || 5002);

// Inventory service base URL (Order Service depends on Inventory for stock checks and prices)
const INVENTORY_SERVICE_URL = process.env.INVENTORY_SERVICE_URL;

if (!INVENTORY_SERVICE_URL) {
    // Warn during startup so the developer knows the service is misconfigured locally
    console.warn('⚠️  INVENTORY_SERVICE_URL is not set. POST /api/orders will fail until configured.');
}

// Database connection (MySQL) using mysql2/promise (async/await)
const dbConfig = {
    host: process.env.DB_HOST,
    user: process.env.DB_USER,
    password: process.env.DB_PASSWORD,
    database: process.env.DB_NAME,
    port: process.env.DB_PORT ? Number(process.env.DB_PORT) : 3306,
    waitForConnections: true,
    connectionLimit: 10,
    queueLimit: 0
};

const pool = mysql.createPool(dbConfig);


function validateCreateOrderPayload(body) {
    if (!body || typeof body !== 'object') {
        return { ok: false, message: 'Request body must be a JSON object.' };
    }

    const { items, customer_name } = body;

    if (customer_name !== undefined && customer_name !== null && typeof customer_name !== 'string') {
        return { ok: false, message: 'customer_name must be a string when provided.' };
    }

    if (!Array.isArray(items) || items.length === 0) {
        return { ok: false, message: 'items must be a non-empty array.' };
    }

    for (const item of items) {
        if (!item || typeof item !== 'object') {
            return { ok: false, message: 'Each item must be an object.' };
        }

        const { product_id, quantity } = item;

        if (!Number.isInteger(product_id) || product_id <= 0) {
            return { ok: false, message: 'Each item.product_id must be a positive integer.' };
        }

        if (!Number.isInteger(quantity) || quantity <= 0) {
            return { ok: false, message: 'Each item.quantity must be a positive integer.' };
        }
    }

    return { ok: true };
}

// Fetch product list from the Inventory Service.
// Used to read current prices (Inventory is the source of truth for catalog data).
async function fetchInventoryProducts() {
    const response = await axios.get(`${INVENTORY_SERVICE_URL}/api/products`, { timeout: 5000 });
    return Array.isArray(response.data) ? response.data : [];
}

/**
 * HELPER 1: CONSOLIDATE ITEMS
 * Combines duplicate product entries in the incoming user cart.
 * * Why this is useful: If a user sends a payload with the same product ID split 
 * across multiple lines, this merges them in-memory to prevent duplicate database 
 * operations or redundant API checks later.
 * * @param {Array} items - Raw array of cart items from req.body (e.g., [{product_id: 1, quantity: 2}, ...])
 * @returns {Map} A Map linking unique product IDs (Key) to their total combined quantities (Value)
 */
function consolidateItems(items) {
    // A Map is used for quick  lookups and updates
    const requestedQtyById = new Map();

    for (const item of items) {
        const productId = Number(item.product_id);
        const quantity = Number(item.quantity);
        
        // Look up if we've already tracked this product in our Map.
        // If it doesn't exist yet, default its current quantity to 0.
        const currentQty = requestedQtyById.get(productId) || 0;
        
        // Add the new quantity onto the existing total and update the Map
        requestedQtyById.set(productId, currentQty + quantity);
    }

    return requestedQtyById;
}

/**
 * HELPER 2: BUILD ORDER DATA
 * Matches the user's requested cart against the official catalog price list, 
 * validates the financial values, and formats the payloads for both local DB and remote services.
 * * @param {Map} requestedQtyById - The clean Map generated by consolidateItems()
 * @param {Map} productById - The official product catalog map fetched from the Inventory Service
 * @returns {Object} An object containing status flags, computed totals, and tailored payloads
 */
function buildOrderData(requestedQtyById, productById) {
    const orderItems = [];      // Rows tailored for our local 'order_items' SQL table
    const inventoryItems = [];  // Minified payload tailored for the external 'inventory-service'

    // Loop through every unique item the customer wants to buy
    for (const [productId, quantity] of requestedQtyById.entries()) {
        // Fetch the corresponding product profile from the official inventory catalog map
        const product = productById.get(productId);

        // DATA VALIDATION: If the user passed an ID that doesn't exist in our active catalog, abort.
        if (!product) {
            return {
                ok: false,
                message: `Unknown product_id or missing price for product_id=${productId}.`
            };
        }

        const unitPrice = Number(product.price);

        // DATA VALIDATION: Ensure the price is a valid, real number (e.g., not NaN, null, or undefined)
        if (!Number.isFinite(unitPrice)) {
            return {
                ok: false,
                message: `Invalid price for product_id=${productId}.`
            };
        }

        // Format and push the row needed to populate our local MySQL database
        orderItems.push({
            product_id: productId,
            quantity,
            unit_price: unitPrice
        });

        // Format and push the minimal data array needed by the Inventory Service endpoint
        inventoryItems.push({
            id: productId,
            quantity
        });
    }

    // FINANCIAL CALCULATION: Aggregate line-item totals using Array.reduce()
    // Accumulator formula: sum = sum + (price * quantity)
    const totalAmount = roundTo2(
        orderItems.reduce((sum, item) => sum + item.unit_price * item.quantity, 0)
    );

    // Return everything packaged up cleanly for the main endpoint controller to use
    return {
        ok: true,
        orderItems,
        inventoryItems,
        totalAmount
    };
}

/**
 * HELPER 3: ROUND TO 2 DECIMAL PLACES
 * Solves JavaScript's native floating-point math issues (e.g., 0.1 + 0.2 = 0.30000000000000004).
 * * How it works: Multiplies the number by 100 to shift the decimals out of the way,
 * rounds it to the nearest whole integer, and then shifts the decimal back by dividing by 100.
 * * @param {Number} amount - The unrounded calculated sum
 * @returns {Number} Financial total rounded precisely to two decimal places
 */
function roundTo2(amount) {
    return Math.round(Number(amount) * 100) / 100;
}

